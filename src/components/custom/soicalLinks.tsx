export default function SocialLinks({ socialLinks }: SocialLinksProps) {
    // 渲染图标
    const renderIcon = (name: string, className: string) => {
        const CustomIcon = CustomIcons[name];
        if (CustomIcon) return <CustomIcon className={className} />;

        const LucideIcon = SOCIAL_ICONS[name] || Globe;
        return <LucideIcon className={className} />;
    };

    // 遍历 socialLinks
    const links = Object.entries(socialLinks);

    return (
        <div className="flex flex-wrap gap-4 mt-6">
            {links.map(([name, url], index) => (
                <a
                    key={index}
                    href={url || '#'}
                    target="_blank"
                    rel="noopener noreferrer"
                    className={`w-10 h-10 rounded-full 
                        border border-gray-800 
                        flex items-center justify-center 
                        transition-all hover:scale-110 
                        text-gray-400 hover:text-white hover:border-white`}
                    title={name}
                >
                    {renderIcon(name, "w-5 h-5")}
                </a>
            ))}
        </div>
    );
}

